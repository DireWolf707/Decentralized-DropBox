<script>
    import Moralis from 'moralis/dist/moralis.min.js';
    import { onMount } from "svelte";
    import { selectedAccount,web3 } from 'svelte-web3';
    import { abi,address } from "../contract";
    import SignUp from "./SignUp.svelte";
    import DropBox from "./DropBox.svelte";
    import Header from "./Header.svelte";
    import Nav from "./Nav.svelte";
    import SideBar from "./SideBar.svelte";

    let contract;
    let userName;
    let currFolder;
    let isLoaded=false;
    let navArray=[];
    let favFiles=[];

    $: onAccountChange($selectedAccount)
    const onAccountChange = ()=>{
        if (isLoaded) {
            login();
        }
    }

    const login = async ()=>{
        isLoaded = false;
        navArray=[];

        const q = new Moralis.Query('CustomUser');
        q.equalTo("address",($selectedAccount).toLowerCase());
        q.include("root")
        const results = await q.find();

        if (results.length == 1){
            userName = results[0].get("name");
            currFolder = results[0].get("root");
            navArray.push(currFolder);
            getFavoriteFiles();
        } else {
            userName = undefined;
            currFolder = undefined;
        }

        isLoaded = true;
    }

    onMount(async ()=>{
        contract = new $web3.eth.Contract(abi,address);
        const serverUrl = "https://andl7efdyriy.usemoralis.com:2053/server";
		const appId = "xnPSSx1r1F7bosll03rFAGS2XjBDvycThrYWy9dc";
		Moralis.start({ serverUrl, appId });
        let user = Moralis.User.current();
		if (!user) {
			try {
				user = await Moralis.authenticate({ signingMessage: "Log in using Moralis" })
			} catch (error) {
				console.log(error);
			}
		}
        login();
    })

    const onFolderEnter = (e)=>{
        currFolder = e.detail;
        navArray = [...navArray,e.detail];
    }

    const onFolderBackward = (e)=>{
        navArray = navArray.slice(0,e.detail);
        currFolder = navArray[e.detail - 1];
    }

    const getFavoriteFiles = async ()=>{
        const q1 = new Moralis.Query('File');
        q1.descending('createdAt');
        q1.equalTo('fav',true);
        q1.equalTo('address',($selectedAccount).toLocaleLowerCase());
        favFiles = await q1.find();
    }
</script>

<Header {userName} />
{#if isLoaded}
    {#if userName}
        <div class="flex mt-8 mx-24">
            <div class="w-1/5">
                <SideBar {favFiles} />
            </div>
            <div class="w-4/5 border-l">
                <Nav {navArray} on:onFolderBackward={onFolderBackward} />
                <DropBox {contract} {currFolder} on:onFolderEnter={onFolderEnter} on:getFavoriteFiles={getFavoriteFiles} />
            </div>
        </div>
    {:else}
        <SignUp on:login={login} />
    {/if}
{:else}
    <div class="text-4xl flex items-center justify-center h-screen mx-auto">Loading... &nbsp;
        <svg role="status" class="mr-2 w-8 h-8 text-gray-200 animate-spin dark:text-gray-600 fill-blue-600" viewBox="0 0 100 101" fill="none" xmlns="http://www.w3.org/2000/svg">
